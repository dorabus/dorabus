Krakov je hlavné mesto Malopoľského vojvodstva. Leží na hornom toku rieky Visla.

Je to všeobecné centrum južného Poľska. Má rozlohu 326,8 km², žije tu 757 500 obyvateľov a v metropolitnej oblasti 1,3 milióna.

Staré mesto je charakteristické početnými historickými stavbami (komplex stredovekých a renesančných budov Starého Mesta, kráľovský zámok Wawel, Sukiennice).

Mesto je sídlom Jagelovskej univerzity a poľskej Akadémie vied.

Po Varšave druhé najväčšie mesto Poľska. Krakov zostal počas druhej svetovej vojny neporušený, vplyv komunistickej vlády sa obmedzil na kyslý dážď.

Bohatá architektúra rozličných historických období sa zachovala a horizont mesta dodnes tvoria veže nespočetných kostolov. 







 



  



