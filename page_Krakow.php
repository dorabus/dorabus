   <h3> Dejiny</h3>



  Mesto Krakov vzniklo približne okolo roku 800 ako obranný hrad Vislanov na križovatke obchodných ciest. Koncom 9. storočia je mesto súčasťou Veľkej Moravy. V 10. storočíbol vybudovaný hrad Wawel a mesto sa stalo súčasťou Poľska (okolo 999). Od roku 1038 je Krakov sídelné mesto Poľska. Mesto prekvitalo v 14. – 16. storočí, kedy bolo členom hanzy, hlavným a od 1320 korunovačným mestom Poľska s prvou poľskou univerzitou (založenou 1364) a známou výrobou súkna. V roku 1596 sa hlavným mestom stala Varšava, ale Krakov ostal korunovačným mestom. V 17. – 18. storočí nastal úpadok. Od roku 1795 mesto patrilo Rakúsku, len v rokoch 1809 – 1815 bolo súčasťou Varšavského kniežatstva a v rokoch 1815 – 1846 hlavným mestom Krakovskej republiky. Po potlačení krakovského povstania bolo opäť pričlenené k Rakúsko-Uhorsku. V roku 1914 sa tu organizovali poľské légie. Od roku 1918 je mesto opäť súčasťou Poľska. Po druhej svetovej vojne sa Krakov stal významným priemyselným strediskom Poľska – Nowa Huta je najväčší metalurgický závod v Poľsku.

 



