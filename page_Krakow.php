   <div class="col-md-7"> <h3>Poloha a geografia</h3>



 

Krakow sa nachádza v južnom Poľsku v údolí rieky Visla. S počtom obyvateľov 758 000 tisíc je tretím najväčším mestom za Varšavou a Lodžou. Rzprestiera sa na 326,8 km 2 a jeho hustota zaľudnenia je 2 318 obyv./km 2.



Je hlavným mestom Malopoľského vojvodstva a vyznačuje sa bohatou kultúrou. Nachádza sa tu až 3000 pamiatkových objektov, 11 vysookých škôl, Poľská akadémia vied, Štátna filharmónia a 14 divadiel.

Krakow bol v roku 2000 zvolený Európskym mestom kultúry.



