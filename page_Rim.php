   <div class="col-md-7"> <h3>Poloha a geografia</h3>



 

Mesto sa rozprestiera v strednej časti Apeninského polostrova na „siedmych pahorkoch“ (Capitolino, Palatino, Qurinale, Viminale, Esquilino, Celio a Aventino) nad riekou Tiber, približne 27 kilometrov od Tyrénskeho mora.

Večné mesto s viac ako dva a pol miliónom obyvateľov je hlavným mestom Talianska a regiónu Lazio. Ťažko povedať, čím mesto priťahuje najviac  – je to arogantné bohatstvo Vatikánu, nekonečnosť námestia Forum či krvavá povesť Kolosea? Staňte sa Rimanom a objavte pôžitky, ktoré mesto ponúka. Od nekonečnej histórie, dýchajúcej zo všetkých strán, po malú, ale intenzívnu intoxikáciu orieškovou zmrzlinou.      



   

