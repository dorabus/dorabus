Rím je hlavné mesto Talianska a regiónu Lazio. Nachádza sa na riekach Tiber a Aniene neďaleko pobrežia Tyrrhenského mora.



Mesto Vatikán je suverénna enkláva v Ríme, je sídlom katolíckej cirkvi a pápeža.



Rím je najväčšie mesto a obec v Taliansku, ako aj jedno z najväčších európskych hlavných miest s rozlohou 1 290 km². Počet obyvateľov obce je 2 553 873, dosahuje však 2 874 038 obyvateľov vrátane občanov bez trvalého bydliska. 

Súčasným starostom je Ignazio Marino (od júna 2013).



Počas svojej asi 2 800 rokov dlhej existencie bol sídlom starovekého Ríma, neskôr pápežských štátov, Talianskeho kráľovstva a Talianskej republiky. Nazýva sa aj „večným mestom“ (tal. la città eterna).

 



  



