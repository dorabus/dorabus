Londýn je hlavné a najväčšie mesto Anglicka a Spojeného kráľovstva. 

Je to tiež najväčšie mesto a metropolitná zóna v Európe. Leží na juhovýchode krajiny pri ústí rieky Temža.

 V súčasnosti je pre svoju polohu a politické podmienky najvýznamnejším finančným centrom na celom svete. 

Produkuje 17% HDP Spojeného kráľovstva. Londýn je spoločne s mestami New York, Tokio a Paríž označovaný ako jedno zo štyroch najdôležitejších miest sveta.

Londýn je domovom mnohých inštitúcií s medzinárodnou pôsobnosťou, ako aj nadnárodných spoločností a organizácií.

Nachádza sa v ňom veľa dôležitých inštitúcií a kultúrnych ustanovizní, vrátane celosvetovo známych divadiel, múzeí, koncertných siení, palácov a ďalších.









 



  



