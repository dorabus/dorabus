   <div class="col-md-7"> <h3>Poloha a geografia</h3>



Veľký Londýn má rozlohu 1 579 km², je to tiež prístav na Temži, čo je rieka prispôsobená pre lodnú dopravu. Londýn bol založený na severnom brehu Temže a mnoho storočí cez ňu viedol iba jediný most, tzv. London Bridge. V 18. storočí boli postavené ďalšie mosty, čo malo za následok expanziu mesta do všetkých smerov, pretože rovná alebo mierne zvlnená krajina okolo rieky nepredstavovala žiadnu prírodnú prekážku. Aj keď v meste je niekoľko kopcov, napríklad Parliament Hill alebo Primrose Hill, sú neveľmi rozľahlé a nepredstavovali pre rozvoj mesta žiadnu prekážku. Vďaka týmto skutočnostiam má Londýn takmer kruhový pôdorys.

Rieka Temža bola kedysi ďaleko širšia a plytšia ako je dnes. Rieka, ktorá preteká Londýnom dnes, bola zovretá do brehov a mnoho prítokov do Temže preteká Londýnskym podzemím. Temža je prílivová rieka a kvôli tomu Londýn bol a stále je ohrozovaný záplavami. Táto hrozba sa postupom času stávala stále reálnejšia vďaka pomalému zdvíhaniu hladiny rieky a tiež kvôli pomalému „nakláňaniu“ Veľkej Británie (na severe zvyšovaniu a na juhu znižovaniu), spôsobené post-ľadovcovým izostatickým zdvíhaním.





