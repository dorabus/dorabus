   <h3> Dejiny</h3>



Prvé zaznamenané meno mesta dnes známeho ako Londýn, bolo Londinium. Bola to vtedy osada, ktorú v roku 43 po Kristovi založili rímske légie cisára Claudia. Gaius Iulius Caesar však prišiel do Británie už o 97 rokov skôr a prekročil rieku Temžu blízko neskoršieho Londýna. V tej dobe ale nezanechal akékoľvek stopy osídlenia.

Na začiatku 7. storočia prišla do Londýna kresťanská misia z Ríma. Bol založený prvý chrám Sv. Pavla – malý drevený kostolík – a mesto sa začalo obnovovať ako obchodný prístav. V roku 851 vtrhli do Londýna Vikingoviaa zničili značnú časť mesta. V roku 886 sa kráľ Alfréd Veľký podľa legendy znovu zmocnil Londýna a vybudoval ho tak, že sa stal najväčším mestom kráľovstva a predstihol dokonca aj hlavné mesto Winchester.

Rok 1066, najznámejší dátum anglických dejín, je rokom poslednej invázie a okupácie cudzími vojskami. Viliam I. Dobyvateľ si nárokoval anglickú korunu a vyslal cez Lamanšský prieliv svoju armádu a v bitke pri Hastingseporazil kráľa Haralda.

