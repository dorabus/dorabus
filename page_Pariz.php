   <h3> Dejiny</h3>



 Názov Paríža vznikol podľa starého galského kmeňa Parisiov. Starí Rimania nazývali Paríž Lutetia Parisiorum – slovo lut znamená močiar a par znamená loď, čln. Mesto sa rozprestiera na troch pahorkoch: Ménilmontant, Montmartre a Butte Sainte-Genevieve. Pôvodné centrum mesta sa nachádzalo na ostrove zvanom l'Ile de la Cité, ktorý sa vytvoril v toku rieky Seina pretekajúcej Parížom. Na tomto ostrove sa okolo roku 300 pred naším letopočtom usadil keltský kmeň Parisii. Všetci neskorší vládcovia mesta prenechali kúsok zo svojej doby a ideí tejto metropole nad Seinou.



Pôvodcom vzniku mnohých veľkolepých parížskych stavieb v 17. storočí je najmä kráľ Ľudovít XIV., nazývaný aj Kráľ Slnko. Azda najznámejšou stavbou je zámok VERSAILLES, ktorý nechal vybudovať približne 23 kilometrov juhozápadne od Paríža vo Versailles. Počas návštevy Paríža by teda bolo priam „hriechom“ nenechať na seba dýchnuť noblesu tohto zámku a nezaspomínať si na časy francúzskeho kráľa Ľudíta XIV. V zámku sa dnes nachádza známa 73 m dlhá, 10,5 m široká a 12,3 m vysoká ZRKADLOVÁ SÁLA celá obložená mramorom a zrkadlami. Avšak aj ostatné sály majú jedinečnú atmosféru. Rovnako impozantne pôsobia i zámocké záhrady.



